#include <stdio.h>

int main() {
  
  int myNum = 5;             // Integer (whole number)
  float myFloatNum = 5.99;   // Floating point number
  char myLetter = 'L';       // Character
  double myDouble =2.1;      // Double

printf("%d\n", myNum);
printf("%f\n", myFloatNum);
printf("%c\n", myLetter);
printf("%lf\n", myDouble);

}
